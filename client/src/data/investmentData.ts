// ---- Types ----

export type Modality =
  | "ADC"
  | "Bispecific"
  | "mAb"
  | "CAR-T"
  | "Gene Therapy"
  | "RNAi"
  | "Small Molecule"
  | "mRNA"
  | "Radiopharmaceutical"
  | "CRISPR"
  | "ASO"
  | "Other";

export type CatalystType = "PDUFA" | "AdCom" | "Phase 3 Readout";

export type Direction = "positive" | "negative";

export interface CompanyPipeline {
  company: string;
  ticker: string;
  marketCapB: number; // in $B
  primaryModality: Modality;
  phase1: number;
  phase2: number;
  phase3: number;
  approved: number;
  areas: string[];
  performancePct: number; // YTD/recent % change
  performanceNote: string;
  tier: 1 | 2 | 3;
}

export interface CatalystEvent {
  date: string; // ISO or "Q1 2026" etc.
  sortDate: string; // for sorting, ISO-ish "2026-04-10"
  company: string;
  ticker: string;
  drug: string;
  eventType: CatalystType;
  indication: string;
  modality: Modality;
  designations?: string;
  month: string; // "April 2026"
}

export interface MnaDeal {
  year: number;
  acquirer: string;
  target: string;
  valueB: number; // in $B
  modality: Modality;
  modalityLabel: string;
  premiumPct: number | null; // null = not disclosed
  keyAsset: string;
  closeNote: string;
}

export interface CatalystMove {
  id: number;
  company: string;
  ticker: string;
  date: string;
  movePct: number; // positive or negative
  catalyst: string;
  drug: string;
  indication: string;
  modality: Modality;
  direction: Direction;
}

export interface ApprovalProbability {
  modality: string;
  modalityKey: Modality;
  phase1to2: number;
  phase2to3: number;
  phase3toNDA: number;
  NDAtoApproval: number;
  loa: number; // overall likelihood of approval from Phase I
}

export interface StructuralTrend {
  id: string;
  title: string;
  subtitle: string;
  description: string;
  dataPoints: string[];
  investmentImplication: string;
  accentColor: string;
  bgColor: string;
}

